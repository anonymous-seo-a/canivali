export function classifyTitle(title: string): CellAssignment {
  let vocabulary_topic_id: string | null = null;
  for (const [pattern, id] of VOCABULARY_PATTERNS) {
    if (typeof pattern === 'string' ? title.includes(pattern) : pattern.test(title)) {
      vocabulary_topic_id = id;
      break;
    }
  }

  let subtopic_topic_id: string | null = null;
  for (const [pattern, id] of SUBTOPIC_PATTERNS) {
    if (pattern.test(title)) {
      subtopic_topic_id = id;
      break;
    }
  }

  // 信頼度
  let confidence = 0;
  if (subtopic_topic_id && vocabulary_topic_id) confidence = 0.7;
  else if (subtopic_topic_id || vocabulary_topic_id) confidence = 0.4;
  else confidence = 0.1;

  return { subtopic_topic_id, vocabulary_topic_id, confidence };
}

export function deriveSubtopicAxis(subtopicTopicId: string | null): string | null {
  if (!subtopicTopicId) return null;
  const head = subtopicTopicId[0];
  return head && /^[A-G]$/.test(head) ? head : null;
}
